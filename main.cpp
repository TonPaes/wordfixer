void substituirPorSemelhante(Corretor &corretor) {
	// Imprimir todas as palavras semelhantes
	auto semelhantes = corretor.obterSemelhantes();
	for (int i = 0; i < semelhantes.size(); ++i)
		cout << i + 1 << ". " << semelhantes[i].getValor() << "\n";

	// Escolher uma das palavras semelhantes
	cout << "\nDigite o numero da palavra desejada: ";
	int opcao;
	cin >> opcao;

	// Armazenar palavras para posterior impressao
	string palavraAntiga = corretor.obterPalavraErrada().getValor();
	string palavraNova = semelhantes[opcao - 1].getValor();

	// Substituir palavra errada por palavra escolhida
	corretor.corrigirPalavra(semelhantes[opcao - 1]);

	// Imprimir resultado da substituicao
	cout << "\"" + palavraAntiga + "\" substituido por \"" + palavraNova + "\".\n";
}

